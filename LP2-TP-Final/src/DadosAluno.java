class DadosAluno {
    private String nome;
    private String idade;
    private String peso;
    private String altura;
    private String objetivo;

    public DadosAluno(String nome, String idade, String peso, String altura, String objetivo) {
        this.nome = nome;
        this.idade = idade;
        this.peso = peso;
        this.altura = altura;
        this.objetivo = objetivo;
    }
}
